    <h2 class="post-header">Calibre y rendimiento</h2>
    <p class="post-paragraph">
      Este cronógrafo equipa el movimiento de cuarzo <strong>Seiko VK63</strong>, un calibre híbrido que <strong>combina precisión electrónica con un módulo mecánico para la función de cronógrafo</strong>. Esto se traduce en una aguja trotadora con un barrido suave, muy similar al de un reloj mecánico.
    </p>
    <p class="post-paragraph">
      La pila ofrece una <strong>reserva de marcha de aproximadamente tres años</strong>. Como era de esperar de un calibre firmado por Seiko, ofrece fiabilidad, precisión y un funcionamiento impecable.
    </p>
    <h2 class="post-header">Materiales y correa</h2>
    <p class="post-paragraph">
      Tanto la caja como el brazalete están fabricados en <strong>acero inoxidable 316L</strong>. El armis, de tipo <strong>President</strong>, tiene un acabado cepillado en sus eslabones con dos líneas pulidas en los laterales del eslabón central. Se siente <strong>sólido, sin holguras</strong> incluso tras meses de uso. El cierre es plegable, con <strong>3 posiciones de microajuste</strong> y el logotipo grabado en bajo relieve.
    </p>
    <p class="post-paragraph">
      También está disponible con <strong>correas de nailon</strong> en distintos colores, lo que permite adaptar el reloj a un uso más informal o deportivo.
    </p>
