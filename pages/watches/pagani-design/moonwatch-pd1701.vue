    <p class="post-paragraph">
      La caja presenta un acabado mayormente <strong>cepillado con una línea pulida</strong> que recorre el canto a 45 grados. El conjunto está bien construido y <strong>el brazalete encaja perfectamente</strong>, sin holguras.
    </p>
    <p class="post-paragraph">
      La corona es <strong>roscada, firmada con el logotipo de Pagani</strong> y con un estriado profundo que facilita el agarre, aunque puede resultar algo molesta si llevas el reloj muy ajustado. Los pulsadores del cronógrafo, situados a ambos lados, tienen un acabado pulido y ofrecen una <strong>pulsación precisa.</strong>
    </p>
    <h2 class="post-header">Bisel y cristal</h2>
    <p class="post-paragraph">
      El bisel es uno de los puntos fuertes del reloj: <strong>está fabricado en cerámica</strong>, un material poco habitual en este rango de precio. Muestra la <strong>escala taquimétrica en blanco</strong> sobre fondo negro, con un ajuste perfecto sobre la caja.
    </p>
    <p class="post-paragraph">
      El cristal es de <strong>zafiro con tratamiento antirreflejos</strong> de buena calidad. La unión con el bisel produce <strong>reflejos azulados</strong> muy estéticos que realzan aún más el diseño del reloj.
    </p>
    <h2 class="post-header">Esfera y diseño del dial</h2>
    <p class="post-paragraph">
      El dial es de color <strong>negro mate</strong>, ligeramente elevado sobre los tres subdiales: el izquierdo para los minutos del crono, el derecho para la hora en formato 24 horas y el inferior para los segundos continuos.
    </p>
    <p class="post-paragraph">
      Bajo las 12 aparece el <strong>logotipo de la marca junto con el nombre y la leyenda "Chronograph"</strong>, un añadido que personalmente me parece innecesario y algo recargado. El fechador, ubicado entre las 4 y las 5, está en <strong>negativo</strong> (números blancos sobre fondo negro) e integrado de forma muy limpia.
    </p>
