    <p class="post-paragraph">
      Los marcadores horarios son <strong>rectangulares y alargados, con lumen blanco</strong> que brilla en tonos verdosos. Los de las 3, 6 y 9 son más cortos para dejar espacio a los subdiales, y se complementan con índices de minutos más pequeños entre ellos.
    </p>
    <p class="post-paragraph">
      Las agujas son <strong>de color blanco y contrastan muy bien con el fondo negro</strong>. Las principales tienen forma de lápiz, con lumen en el centro, mientras que la aguja del crono tiene forma de pica, con una punta luminosa. Las agujas de los subdiales no llevan lumen.
    </p>
    <h2 class="post-header">Lumen</h2>
    <p class="post-paragraph">
      Este es, quizás, el punto más débil del reloj. <strong>El lumen es débil y su duración es limitada</strong>. En comparación con otros modelos de la misma marca, como el homenaje al Black Bay, el rendimiento es inferior. Aun así, <strong>cumple de forma básica</strong> en situaciones de poca luz.
    </p>
    <h2 class="post-header">Tapa trasera</h2>
    <p class="post-paragraph">
      La tapa es <strong>roscada</strong>, lo que garantiza una resistencia al agua de <strong>100 metros</strong>. El diseño es elegante, con un anillo exterior grabado en bajo relieve con especificaciones técnicas, y <strong>un grabado central que representa una escena espacial</strong>, en clara referencia al Moonwatch original. Este tipo de detalles le añade un plus de carácter al reloj.
    </p>
    <h2 class="post-header">Medidas y comodidad</h2>
    <p class="post-paragraph">
      El PD-1701 mide <strong>47 mm de largo, 40 mm de diámetro y 14 mm de grosor</strong>. Aunque es algo grueso para un cuarzo, en la muñeca resulta bastante <strong>equilibrado</strong>. El peso total con armis es de 140 gramos, lo que lo hace sentir sólido pero no incómodo.
    </p>
